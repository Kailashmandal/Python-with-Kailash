# min and max function for the list

# min(list_name) : gives the element which is smallest in list
#max(list_name) : gives the largest element

number= [2,3,4,5,66,7,888,5,4,9,-98]

print(
    f"the greatest element in the list is { max(number) }"
)


print(
    f"the smallest element in the list is { min(number) }"
)
